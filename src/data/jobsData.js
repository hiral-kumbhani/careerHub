// Generate 840 items (10 items * 84 pages)
const generateJobs = () => {
  const jobs = [];
  // We want to make sure every category has at least a few jobs.
  // We will cycle through the categories array as we generate jobs.

  for (let i = 0; i < 840; i++) {
    const baseJob = realJobs[i % realJobs.length];

    // Pick a category from the list in a round-robin fashion to ensure full coverage
    const extraCategory = categories[i % categories.length];

    // Also pick a random one just for more variety
    const randomCategory = categories[Math.floor(Math.random() * categories.length)];

    let newCategoryString = baseJob.category;

    // Append the guaranteed category if not already present
    if (!newCategoryString.includes(extraCategory)) {
      newCategoryString += `, ${extraCategory}`;
    }

    // Append the random category if not already present
    if (!newCategoryString.includes(randomCategory)) {
      newCategoryString += `, ${randomCategory}`;
    }

    jobs.push({
      ...baseJob,
      id: i + 1,
      // Add a slight variation to the title for duplicate entries to make it clearer
      title: i < realJobs.length ? baseJob.title : `${baseJob.title} (Archive ${Math.floor(i / realJobs.length)})`,
      category: newCategoryString
    });
  }
  return jobs;
};

